
    function openPopup(id) {
        var popup = document.getElementById(id);
        if (popup) {
            popup.style.display = "block";
        }
    }

    function closePopup(id) {
        var popup = document.getElementById(id);
        if (popup) {
            popup.style.display = "none";
        }
    }

